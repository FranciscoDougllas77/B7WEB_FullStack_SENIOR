// Comentários em JavaScript são usados para explicar o código, deixar anotações ou desabilitar trechos temporariamente.
// Eles não são executados pelo interpretador e servem apenas para facilitar a leitura e manutenção do código.

// Existem dois tipos de comentários:

// 1. Comentário de linha única: usa duas barras (//)
// Exemplo:
console.log("Olá!"); // Este é um comentário de linha única

// 2. Comentário de múltiplas linhas: começa com /* e termina com */
// Exemplo:
/*
  Este é um comentário
  de múltiplas linhas.
  Pode ocupar várias linhas do código.
*/

console.log("Comentários ajudam a documentar o código!");
